# 格式化百分数

print(format(0.1623445,'%'))    # 将小数格式化为百分数
print(format(0.1623445,'.2%'))  # 格式化为保留2位小数的百分数
print(format(0.01623445,'.6%')) # 格式化为保留6位小数的百分数
print(format(2/16,'.2%'))       # 格式化为保留2位小数的百分数
print(format(0.1623445,'.0%'))  # 格式化为保留整数的百分数
print(format(0.1623445,'8.6%')) # 格式化为保留6位小数的八位百分数
