# 输出数值

print(1314)                    # 直接输出整数,可不带双引号或单引号
print(12.22)                   # 直接输出浮点数
print(10/3)                    # 可以包含算术表达式
print(100 * 3.13 + 60)         # 可以包含算术表达式
print(2, 0, 2, 0)              # 使用“，”连接要输出的数值
print(192, 168, 1, 1, sep='.') # 使用“.”连接输出数值，树枝间“.”间隔
